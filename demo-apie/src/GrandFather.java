public   class GrandFather {
  private  double  height;
  private double  weight;

  public GrandFather(){
    
  }


  public GrandFather(double height, double weight){
    this.height =height;
    this.weight =weight;
  }

  public double  getHeight(){
    return this.height;
  }

  public double getWeight(){
    return this.weight;
  }
  
}
