public class Account {
  private String type;
  private double balance;

  // Instance Method
  //Getter
  public String getType(){
    return this.type;
  }

  
  public double  getBalance(){
    return this.balance;
  }

  //Setter
  //void -> no return value
  public void setType(String type){
    this.type = type;
  }

  public void setBalance(double balance){
    this.balance = balance;
  }


  
}
